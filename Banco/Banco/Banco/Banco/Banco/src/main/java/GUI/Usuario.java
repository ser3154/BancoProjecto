    private void mostrarSaldoCliente() {
        try (Connection connection = conexionBD.crearConexion();
             PreparedStatement pstmt = connection.prepareStatement("SELECT saldo FROM Cuentas WHERE ID_cliente = ?");
        ) {
            pstmt.setLong(1, idCliente);

            try (ResultSet rs = pstmt.executeQuery()) {
                if (rs.next()) {
                    double saldoDisponible = rs.getDouble("saldo");
                    // Se muestra el saldo en la etiqueta "saldo"
                    saldo.setText("$ "+String.valueOf(saldoDisponible));
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al obtener el saldo del cliente: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
    
    
    
    
    
