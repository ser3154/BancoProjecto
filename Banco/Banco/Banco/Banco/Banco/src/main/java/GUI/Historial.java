private void mostrarHistorialOperaciones() {
        try (Connection connection = conexionBD.crearConexion();
             PreparedStatement pstmt = connection.prepareStatement("SELECT * FROM Operaciones WHERE ID_cliente = ?");
        ) {
            pstmt.setLong(1, idClienteSesion);

            try (ResultSet rs = pstmt.executeQuery()) {
                // Crear un modelo de tabla para almacenar los datos de las operaciones
                DefaultTableModel modeloTabla = new DefaultTableModel();
                modeloTabla.addColumn("ID Operaci贸n");
                modeloTabla.addColumn("Tipo de Operaci贸n");
                modeloTabla.addColumn("Monto");
                modeloTabla.addColumn("Fecha y Hora");

                while (rs.next()) {
                    // Recuperar detalles de la operaci贸n
                    int idOperacion = rs.getInt("ID_operacion");
                    String tipoOperacion = rs.getString("tipo_operacion");
                    double monto = rs.getDouble("monto");
                    Timestamp Fecha = rs.getTimestamp("Fecha");

                    // Agregar una fila con los detalles de la operaci贸n al modelo de tabla
                    Object[] fila = {idOperacion, tipoOperacion, monto, Fecha};
                    modeloTabla.addRow(fila);
                }

                // Establecer el modelo de tabla en la tablaOperaciones
                tablaOperaciones.setModel(modeloTabla);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al obtener el historial de operaciones: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }





